

/**
 * The Class Message.
 */
public class Message {

	/** The text. */
	private final String text;

	/**
	 * Instantiates a new message.
	 *
	 * @param text the text
	 */
	public Message(String text) {
		// this.sender = sender;
		this.text = text;
	}

	/**
	 * Gets the text.
	 *
	 * @return the text
	 */
	public String getText() {
		return text;
	}

	/* 
	 * toString method of the class
	 */
	public String toString() {
		return text;
	}
}
